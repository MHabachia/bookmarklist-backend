package Bookmarklist;

public class bookmark_entry {
    String title;
    boolean done;

    public bookmark_entry() {}

    public bookmark_entry(String title) {
        this.title = title;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public boolean isDone() {
        return done;
    }

    public void setDone(boolean done) {
        this.done = done;
    }
}


